// Increment   ++
// Decrement  --

let V: number = 10;

V++;    //Post Increment    
console.log(V);



let L: number = 10;

++L;   // Pre-Increment
console.log(L);




let S: number = 10;

let FirstResultNumber: number = S++;
console.log(FirstResultNumber);
console.log(S);




let J: number = 10;

let SecondResultNumber: number = ++J;
console.log(SecondResultNumber);
console.log(J);




let D: number = 20;

D--;  // Post Decrement
console.log(D);




let M: number = 20;

--M;  // Pre Decrement
console.log(M);




let P: number = 20;

let FirstResult: number = P--;

console.log(FirstResult);
console.log(P);



let R: number = 20;

let SecondResult: number = --R;

console.log(SecondResult);
console.log(R);





