// Ternary/Conditional operator

//  exp ?  res1 : res2;


//Example 1

let FirstValue: number = 100, SecondValue: number = 200;

let FinalValue: number = (FirstValue > SecondValue) ? FirstValue : SecondValue;

console.log(FinalValue);          //200


//Example 2:

let PersonAge: number = 30;

let PersonAgeResult: string = (PersonAge >= 18) ? "Adult" : "Minor";

console.log(PersonAgeResult);  // Adult
