//do-while loop : A do-while loop always executes at least once before checking the condition.


//Example 1:  1....5

let IG: number = 1;

do {
    console.log(IG);
    IG++;
} while (IG <= 5);



//Example 2: 10 9 8 .......1  (decending order)


let IH: number = 10;

do {
    console.log(IH);
    IH--;
} while (IH >= 1);